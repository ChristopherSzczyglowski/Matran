                'K'  , 2, 'S'  , 2, 'CW' , 2, 'M_A', 2, ...
                'M_B', 2, 'N_A', 2, 'N_B', 2}, ...
                'IDProp'     , 'PID', ...
                'Connections', {'MID', 'bulk.Material', 'Materials'}, ...
